// 关卡管理器
const LevelManager = {
    // 获取所有关卡
    getAllLevels() {
        return allLevels;
    },

    // 获取指定难度的关卡
    getLevelsByDifficulty(difficulty) {
        return allLevels[difficulty] || [];
    },

    // 获取指定关卡
    getLevel(id) {
        for (const difficulty of Object.keys(allLevels)) {
            const level = allLevels[difficulty].find(l => l.id === id);
            if (level) {
                return { ...level, difficulty };
            }
        }
        return null;
    },

    // 获取下一关
    getNextLevel(currentId) {
        for (const difficulty of Object.keys(allLevels)) {
            const levels = allLevels[difficulty];
            const index = levels.findIndex(l => l.id === currentId);
            if (index !== -1 && index < levels.length - 1) {
                return levels[index + 1];
            }
        }
        return null;
    },

    // 获取上一关
    getPrevLevel(currentId) {
        for (const difficulty of Object.keys(allLevels)) {
            const levels = allLevels[difficulty];
            const index = levels.findIndex(l => l.id === currentId);
            if (index > 0) {
                return levels[index - 1];
            }
        }
        return null;
    },

    // 获取关卡总数
    getTotalCount() {
        let total = 0;
        for (const difficulty of Object.keys(allLevels)) {
            total += allLevels[difficulty].length;
        }
        return total;
    },

    // 获取难度关卡数
    getCountByDifficulty(difficulty) {
        return allLevels[difficulty]?.length || 0;
    }
};

